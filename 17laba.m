% Вектор стоимости кормов
f = [30; 20; 50];

% Матрица коэффициентов для неравенств
A = [-50, -20, -180;
     -6,  -4,  -3;
     -2,  -1,  -1];

% Вектор правых частей
b = [-2000; -120; -40];

% Нижние границы переменных
lb = [0; 0; 0];

% Решение задачи линейного программирования
[x, cost] = linprog(f, A, b, [], [], lb);

% Вывод решения
disp('Количество сена, силоса, концентрата:');
disp(x);
disp('Минимальная стоимость:');
disp(cost);

